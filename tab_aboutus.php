  <h1>About TheTVDB.com</h1>
	<p class="longtext">This website strives to be an open and complete database of television shows from all around the globe. The site includes series, season 
	 and episode banners created by and for this community. All of the data and images are available through an xml interface so that they can be
	 incorporated into various HTPC software and plugins.</p>

	<p class="longtext">This site is an amalgamation of a database website by Josh Walters (walts81) and a banner site by Richard (t0ffluss). The redesign and 
	programming are currently being done by <a href="mailto:<?php echo cryptemail('scott-tvdb@zsori.com');?>">Scott Zsori</a> (szsori) and previously
	Paul Taylor (polargeek)</p>
	
	<p class="longtext">While this site and it's predecessors were originally developed for a series of plug-ins for <a href="http://meedio.com/">Meedio</a> we are
	happy that it is now available to all.</p> 

	<p class="longtext">The source code and database schema for this site is licensed under the <a href="http://www.gnu.org/copyleft/gpl.html">GPL</a> and is available from 
	<a href="http://sourceforge.net/projects/tvdb/">SourceForge</a>.</p> 

